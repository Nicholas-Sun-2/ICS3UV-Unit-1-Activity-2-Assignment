package main

import "fmt"

func main() {
	// This line uses the area of a circle formula (a = Pi * r^2) with Pi approximated as 3.14.
	fmt.Println("The area of a circle with a radius of 14 cm = " + fmt.Sprint(14 * 14 * 3.14) + " cm^2.")
	
	fmt.Println("\nDone.")
}

